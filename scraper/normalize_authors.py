def normalize():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    print(f"Database: {DB_PATH}")
    print(f"Mapping: {len(AUTHOR_MAP)} entries\n")

    total_updated = 0
    for wrong, correct in AUTHOR_MAP.items():
        cursor.execute(
            "UPDATE articles SET author = ? WHERE author = ?",
            (correct, wrong),
        )
        count = cursor.rowcount
        if count > 0:
            target = correct if correct else "(пусто)"
            print(f"  {wrong!r:55s} → {target!r:30s}  ({count} шт)")
            total_updated += count

    conn.commit()
    print(f"\nОбновлено: {total_updated} статей")

    # Show final author list
    authors = cursor.execute("""
        SELECT author, COUNT(*) as cnt
        FROM articles
        WHERE author IS NOT NULL AND author != ''
        GROUP BY author ORDER BY cnt DESC
    """).fetchall()
    print(f"\nАвторы после нормализации ({len(authors)}):")
    for a, c in authors:
        print(f"  [{c:6d}] {a}")

    conn.close()


if __name__ == "__main__":
    normalize()
